

    function myFunction() {
          document.getElementById("pl1").innerHTML = "Sports Cap";
       
        const priceField = document.getElementById('taka');
        const priceAmountString = priceField.innerText;
       
        const newPriceAmount = parseFloat(priceAmountString);

        const priceTotalElement = document.getElementById('tp');
        const previousPriceTotalString = priceTotalElement.innerText;
        const previousPriceTotal= parseFloat(previousPriceTotalString);

        const currentPriceTotal = previousPriceTotal + newPriceAmount;
        priceTotalElement.innerText = currentPriceTotal; 

}






